package dp.aula6;

public class ItemDaNota {

	String descricao;
	double valor;
	
	public ItemDaNota(String descricao, double valor) {
		super();
		this.descricao = descricao;
		this.valor = valor;
	}

	public String getDescricao() {
		return descricao;
	}

	public double getValor() {
		return valor;
	}
	
	
	
	

}
